const { WorldGenerationType, Grass, Air, Bedrock, Dirt} = require("@greenfrog/mc-enums")

const Generator = require("./Generator")

class Flat extends Generator {
	name = WorldGenerationType.Flat

	get_chunk_data() {
		const chunk_data = Buffer.alloc(16 * 256 * 16)

		for (let x = 0; x < 16; x++) {
			for (let y = 0; y < 256; y++) {
				for (let z = 0; z < 16; z++) {
					const index = y * 16 * 16 + z * 16 + x

					if (x > 14 && y > -1 && y < 16) {
						chunk_data[index] = new Grass().get_id()
					} else if (y < 18 && x < 11) {
						chunk_data[index] = new Air().get_id()
					} else if (y < 17 && x < 12) {
						chunk_data[index] = new Bedrock().get_id()
					} else {
						chunk_data[index] = new Dirt().get_id()
					}
				}
			}
		}

		return chunk_data
	}
}

module.exports = Flat
