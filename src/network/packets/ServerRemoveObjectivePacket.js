const PacketConstructor = require("./PacketConstructor");

class ServerRemoveObjectivePacket extends PacketConstructor {
	name = "remove_objective";
	/** @type {string} */
	objective_name;

	writePacket(client) {
		client.queue(this.name, {
			objective_name: this.objective_name,
		});
	}
}

