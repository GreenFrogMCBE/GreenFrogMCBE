const Packet = require("./Packet");

class ServerItemComponentPacket extends Packet {
	name = "item_component";
	
	/** @type {any | undefined} */
	entries;

	/**
	 * @param {import("Frog").Player} player
	 */
	writePacket(player) {
		player.queue(this.name, {
			entries: this.entries,
		});
	}
}

module.exports = ServerItemComponentPacket;
