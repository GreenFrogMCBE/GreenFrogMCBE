const PacketConstructor = require("./PacketConstructor");

class ServerToastRequestPacket extends PacketConstructor {
	name = "toast_request";
	/** @type {string} */
	title;
	/** @type {string} */
	message;

	writePacket(client) {
		client.queue(this.name, {
			title: this.title,
			message: this.message,
		});
	}
}

module.exports = ServerToastRequestPacket;
