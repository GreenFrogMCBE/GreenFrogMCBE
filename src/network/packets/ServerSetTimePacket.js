const Packet = require("./Packet")

class ServerSetTimePacket extends Packet {
	name = "set_time"

	/** @type {number | undefined} */
	time

	/**
	 * @param {import("Frog").Player} player
	 */
	write_packet(player) {
		player.queue(this.name, {
			time: this.time,
		})
	}
}

module.exports = ServerSetTimePacket
