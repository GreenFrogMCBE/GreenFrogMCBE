const PacketConstructor = require("./PacketConstructor");

class ServerTransferPacket extends PacketConstructor {
	name = "transfer";
	/** @type {string} */
	server_address;
	/** @type {number} */
	port;

	writePacket(client) {
		client.queue(this.name, {
			server_address: this.server_address,
			port: this.port,
		});
	}
}

module.exports = ServerTransferPacket;
