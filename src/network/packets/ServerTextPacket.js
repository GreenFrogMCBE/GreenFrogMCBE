const PacketConstructor = require("./PacketConstructor");

class ServerTextPacket extends PacketConstructor {
	name = "text";
	/** @type {number} */
	message;
	/** @type {import("../../api/types/Text")} */
	type;
	/** @type {boolean} */
	needs_translation;
	/** @type {string} */
	source_name;
	/** @type {string} */
	xuid;
	/** @type {string} */
	platform_chat_id;

	writePacket(client) {
		client.queue(this.name, {
			type: this.type,
			needs_translation: this.needs_translation,
			source_name: this.source_name,
			message: this.message,
			xuid: this.xuid,
			platform_chat_id: this.platform_chat_id,
		});
	}
}

module.exports = ServerTextPacket;
