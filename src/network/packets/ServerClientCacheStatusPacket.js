const Packet = require("./Packet");

class ServerClientCacheStatusPacket extends Packet {
	name = "client_cache_status";
	
	/** @type {boolean | undefined} */
	enabled;

	/**
	 * @param {import("Frog").Player} player
	 */
	writePacket(player) {
		player.queue(this.name, { enabled: this.enabled });
	}
}

