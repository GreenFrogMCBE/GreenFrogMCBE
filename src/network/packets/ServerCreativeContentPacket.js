const PacketConstructor = require("./PacketConstructor");

class ServerCreativeContentPacket extends PacketConstructor {
	name = "creative_content";
	/** @type {Array<any>} */
	items;

	writePacket(client) {
		client.queue(this.name, {
			items: this.items,
		});
	}
}

module.exports = ServerCreativeContentPacket;
